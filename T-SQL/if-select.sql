DECLARE
	@LIMITE_MAX FLOAT,
	@LIMITE_ATUAL FLOAT,
	@BAIRRO VARCHAR(30);

SET @LIMITE_MAX = 50000;

SET @BAIRRO = 'Água Santa';

SELECT
	@LIMITE_ATUAL = SUM([LIMITE DE CREDITO])
FROM [TABELA DE CLIENTES]
WHERE BAIRRO = @BAIRRO; 

IF @LIMITE_MAX <= @LIMITE_ATUAL
	PRINT 'VALOR PASSOU, BUM';
ELSE
	PRINT 'VALOR DENTRO, BOM';

