CREATE TABLE TAB_FATURAMENTE 
(DATA_VENDA DATE NULL,
TOTAL_VENDA FLOAT NULL);

SELECT
	ID_PRODUTOS_ITENS,
	ID_VENDEDORES_ITENS,
	QUANTIDE, 
	PREÇO
FROM ITENS_VENDIDOS;

SELECT * FROM TABELA_VENDAS;

SELECT
	SUM(IV.PREÇO * IV.QUANTIDE) AS TOTAL_VENDA,
	TV.DATA_VENDA
FROM TABELA_VENDAS TV
INNER JOIN ITENS_VENDIDOS IV
ON TV.ID_TABELA_VENDAS = IV.ID_VENDEDORES_ITENS
GROUP BY TV.DATA_VENDA

