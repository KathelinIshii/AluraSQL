USING SUCOS_FRUTAS.dbo.TABELA_DE_PRODUTOS B
ON A.ID_PRODUTOS = B.CODIGO_DO_PRODUTO
WHEN MATCHED THEN
UPDATE SET A.PRECO_LISTA = B.PRECO_DE_LISTA;

